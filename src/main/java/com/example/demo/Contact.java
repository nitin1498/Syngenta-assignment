package com.example.demo;

public class Contact {
	public String fname;
	public String lname;
	public String address;
	public String city;
	public String code;
	public String pin;
	public Contact(String fname, String lname, String address, String city, String code, String pin) {
		super();
		this.fname = fname;
		this.lname = lname;
		this.address = address;
		this.city = city;
		this.code = code;
		this.pin = pin;
	}
	
	
}
